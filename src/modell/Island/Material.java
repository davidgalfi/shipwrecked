package modell.Island;

public class Material{
    // How many are of them on the island
    private int wood_number;
    private int fruit_wood_number;
    private int water_number;
    private int clay_number;

    // How many of them known by the player
    private int know_wood_number;
    private int know_fruit_wood_number;
    private int know_water_number;
    private int know_clay_number;

    // getters and setters:
    // wood number
    public int getWood_number() {
        return wood_number;
    }
    public void setWood_number(int wood_number) {
        this.wood_number = wood_number;
    }
    // fruit wood number
    public int getFruit_wood_number() {
        return fruit_wood_number;
    }
    public void setFruit_wood_number(int fruit_wood_number) {
        this.fruit_wood_number = fruit_wood_number;
    }
    // water number
    public int getWater_number() {
        return water_number;
    }
    public void setWater_number(int water_number) {
        this.water_number = water_number;
    }
    // clay number
    public int getClay_number() {
        return clay_number;
    }
    public void setClay_number(int clay_number) {
        this.clay_number = clay_number;
    }
    // know wood number
    public int getKnow_wood_number() {
        return know_wood_number;
    }
    public void setKnow_wood_number(int know_wood_number) {
        this.know_wood_number = know_wood_number;
    }
    // know fruit wood number
    public int getKnow_fruit_wood_number() {
        return know_fruit_wood_number;
    }
    public void setKnow_fruit_wood_number(int know_fruit_wood_number) {
        this.know_fruit_wood_number = know_fruit_wood_number;
    }
    // know water number
    public int getKnow_water_number() {
        return know_water_number;
    }
    public void setKnow_water_number(int know_water_number) {
        this.know_water_number = know_water_number;
    }
    // know clay number
    public int getKnow_clay_number() {
        return know_clay_number;
    }
    public void setKnow_clay_number(int know_clay_number) {
        this.know_clay_number = know_clay_number;
    }
}
